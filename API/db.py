# imports |------------------------------------------------------------------------------------------------------------|
from pymongo import MongoClient
from flask import current_app, g
# |--------------------------------------------------------------------------------------------------------------------|


def get_db() -> MongoClient:
    if 'db' not in g:
        g.db = MongoClient(current_app.config['MONGO_URI'])
    return g.db


